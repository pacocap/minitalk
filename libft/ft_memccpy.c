#include "libft.h"

void	*ft_memccpy(void *dest, void *src, int c, int n)
{
	int				i;
	unsigned char	*dest2;
	unsigned char	*src2;

	i = 0;
	dest2 = (unsigned char *)dest;
	src2 = (unsigned char *)src;
	while (i < n)
	{
		dest2[i] = src2[i];
		i++;
		if (dest2[i - 1] == (unsigned char)c)
			return (&dest2[i]);
	}
	return (0);
}
